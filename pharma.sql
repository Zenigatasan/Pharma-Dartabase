drop table if exists prescription;
drop table if exists fill;
drop table if exists inventory;
drop table if exists patient;
drop table if exists doctor;
drop table if exists retail_pharma;
drop table if exists drug;
drop table if exists pharmaceutical;
drop table if exists supervisor;

create table doctor(
    id int primary key,
    name varchar(25),
    ssn  char(11),
    specialty varchar(20)
);
create table patient(
    id int primary key,
    name varchar(25) not null,
    address varchar(100) not null,
    ssn char(11) not null,
    dob char(10) not null,
    doc_id int not null,
    foreign key (doc_id) references doctor(id)
);
create table drug(
    rxid numeric(5,0) check (rxid > 10000),
    generic_name varchar(50) not null,
    trade_name varchar(50),
    primary key(rxid)
);
create table prescription(
    rxid numeric(5,0),
    patient_id int not null,
    doc_id int not null,
    qty numeric(5,3) not null,
    refills int not null,
    date_pr char(10) not null,
    primary key(rxid,patient_id),
    foreign key(rxid) references drug(rxid),
    foreign key(patient_id) references patient(id),
    foreign key(doc_id) references doctor(id)
);

create table supervisor(
    id int primary key,
    name varchar (25) not null,
    email varchar(45) not null,
    phone varchar (15) 
);
create table pharmaceutical(
    id int primary key,
    name varchar(30) not null,
    sup_id int not null,
    foreign key (sup_id) references supervisor(id)
);

create table retail_pharma(
    id int primary key,
    name varchar(30) not null,
    address varchar(100) not null,
    phone varchar(15) not null
);

create table inventory(
    rxid int not null,
    retail_id int not null,
    qty int not null,
    price numeric (5,2),
    primary key(rxid,retail_id),
    foreign key(rxid) references drug(rxid),
    foreign key(retail_id) references retail_pharma(id)
);

create table fill( 
    rxid int not null,
    pharma int not null,
    retail int not null,
   qty numeric(6,3) not null,
    patient int,
    fill_date char(10) not null,
    foreign key(rxid) references drug(rxid),
    foreign key(pharma) references pharmaceutical,
    foreign key(patient) references patient(id)
);



